def sty(size, clr, align=TA_RIGHT, sb=4, sa=3, leading=None):
    return ParagraphStyle('x', fontName='F', fontSize=size, textColor=clr,
        alignment=align, spaceBefore=sb, spaceAfter=sa,
        leading=leading or size*1.65)

